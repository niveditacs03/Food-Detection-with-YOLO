class FoodCategoryClassifier:
    RICE_ITEMS = {'biryani', 'white rice', 'poha', 'whiterice'}
    LIQUID_CURRIES_BOWL = {
        'shahi paneer', 'rajma curry', 'mutton curry', 'kheer', 'dum aloo',
        'aloo gobi', 'aloo masala', 'coconut chutney', 'bhindi masala',
        'fish curry', 'dal', 'shahipaneer', 'rajmacurry', 'muttoncurry',
        'dumaloo', 'aloogobi', 'aloomasala', 'coconutchutney', 'bhindimasala',
        'fishcurry', 'chole'
    }
    LIQUIDS_IN_CUP = {'lassi', 'chai'}
    ITEMS = {'idli', 'rasmalai', 'dosa', 'ghevar', 'kulfi', 'jalebi', 'gulabjamun', 'onionpakoda', 'samosa', 'bhatura'}

    @classmethod
    def classify_food(cls, food_name: str) -> Tuple[str, str]:
        food_lower = food_name.lower().strip()
        if food_lower in cls.RICE_ITEMS:
            return 'rice', 'portion_estimation'
        if food_lower in cls.LIQUID_CURRIES_BOWL:
            return 'liquid_curries_bowl', 'liquid_estimation_bowl'
        if food_lower in cls.LIQUIDS_IN_CUP:
            return 'liquids_in_cup', 'liquid_estimation_cup'
        if food_lower in cls.ITEMS:
            return 'items', 'item_counting'
        return 'other_items', 'not_implemented'


