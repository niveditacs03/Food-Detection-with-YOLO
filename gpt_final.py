# SECTION 2: IMPROVED LIQUID VOLUME ESTIMATOR
# ============================================================================
class LiquidVolumeEstimator:
    """Improved container/cup/bowl capacity estimator.

    Main changes vs the older og_file logic:
    - uses consistent px_to_mm scaling everywhere
    - uses top view to estimate opening ratio when available
    - avoids the old partial '* 3' geometry inflation
    - reconstructs full side profile rather than sparse polynomial points
    - does not silently assume the container is always 100% full
    """

    def __init__(self, px_to_mm: float = 0.3438, debug: bool = False):
        self.px_to_mm = float(px_to_mm)
        self.debug = debug

    def preprocess(self, image: np.ndarray) -> np.ndarray:
        if image is None:
            raise ValueError("Input image is None")
        if image.ndim == 3:
            gray = cv2.cvtColor(image, cv2.COLOR_BGR2GRAY)
        else:
            gray = image.copy()
        gray = cv2.GaussianBlur(gray, (5, 5), 0)
        gray = cv2.medianBlur(gray, 5)
        return gray

    def _largest_mask_from_binary(self, binary: np.ndarray) -> Tuple[np.ndarray, np.ndarray]:
        contours, _ = cv2.findContours(binary, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_NONE)
        if not contours:
            raise ValueError("No contour detected")
        contour = max(contours, key=cv2.contourArea)
        mask = np.zeros_like(binary)
        cv2.drawContours(mask, [contour], -1, 255, -1)
        return mask, contour

    def segment_container(self, image: np.ndarray) -> Tuple[np.ndarray, np.ndarray]:
        gray = self.preprocess(image)
        candidates = []
        for mode in [cv2.THRESH_BINARY + cv2.THRESH_OTSU, cv2.THRESH_BINARY_INV + cv2.THRESH_OTSU]:
            _, th = cv2.threshold(gray, 0, 255, mode)
            th = cv2.morphologyEx(th, cv2.MORPH_CLOSE, np.ones((5, 5), np.uint8), iterations=2)
            th = cv2.morphologyEx(th, cv2.MORPH_OPEN, np.ones((3, 3), np.uint8), iterations=1)
            try:
                mask, contour = self._largest_mask_from_binary(th)
                area = cv2.contourArea(contour)
                candidates.append((area, mask, contour))
            except Exception:
                pass
        if not candidates:
            edges = cv2.Canny(gray, 60, 160)
            edges = cv2.dilate(edges, np.ones((3, 3), np.uint8), iterations=1)
            edges = cv2.morphologyEx(edges, cv2.MORPH_CLOSE, np.ones((7, 7), np.uint8), iterations=2)
            return self._largest_mask_from_binary(edges)
        candidates.sort(key=lambda x: x[0], reverse=True)
        _, mask, contour = candidates[0]
        return mask, contour

    def _mask_profile(self, mask: np.ndarray) -> Dict[str, np.ndarray]:
        ys = np.where(mask > 0)[0]
        if ys.size == 0:
            raise ValueError("Empty mask")
        y_top, y_bottom = int(ys.min()), int(ys.max())

        rows, lefts, rights, widths = [], [], [], []
        for y in range(y_top, y_bottom + 1):
            xs = np.where(mask[y] > 0)[0]
            if xs.size < 3:
                continue
            rows.append(y)
            lefts.append(xs.min())
            rights.append(xs.max())
            widths.append(xs.max() - xs.min())

        rows = np.asarray(rows)
        lefts = np.asarray(lefts, dtype=float)
        rights = np.asarray(rights, dtype=float)
        widths = np.asarray(widths, dtype=float)
        centers = (lefts + rights) / 2.0
        radii = widths / 2.0

        #smoothing
        if len(radii) >= 9:
            k = 9
            kernel = np.ones(k) / k
            radii = np.convolve(radii, kernel, mode="same")
            centers = np.convolve(centers, kernel, mode="same")

        return {
            "rows": rows,
            "lefts": lefts,
            "rights": rights,
            "widths": widths,
            "centers": centers,
            "radii": radii,
            "y_top": y_top,
            "y_bottom": y_bottom,
        }

    def _robust_band_stat(self, arr: np.ndarray, frac: float, top: bool = True) -> float:
        n = max(3, int(len(arr) * frac))
        band = arr[:n] if top else arr[-n:]
        return float(np.median(band))

    def _infer_container_type(self, rim_diam_mm: float, base_diam_mm: float, height_mm: float, force: Optional[str]) -> str:
        if force is not None:
            return force
        wh = rim_diam_mm / max(height_mm, 1e-6)
        rb = rim_diam_mm / max(base_diam_mm, 1e-6)
        if wh >= 1.0 or rb >= 1.05:
            return "bowl"
        return "cup"

    def _opening_from_top_view(self, top_view_path: Optional[str]) -> Dict[str, Any]:
        if not top_view_path:
            return {
                "outer_diameter_px": None,
                "inner_diameter_px": None,
                "inner_outer_ratio": None,
                "opening_mask": None,
            }
        img = cv2.imread(top_view_path)
        if img is None:
            raise FileNotFoundError(f"Top-view image not found: {top_view_path}")

        gray = self.preprocess(img)
        circles = []
        for mode in [cv2.THRESH_BINARY + cv2.THRESH_OTSU, cv2.THRESH_BINARY_INV + cv2.THRESH_OTSU]:
            _, th = cv2.threshold(gray, 0, 255, mode)
            th = cv2.morphologyEx(th, cv2.MORPH_CLOSE, np.ones((5, 5), np.uint8), iterations=2)
            contours, _ = cv2.findContours(th, cv2.RETR_TREE, cv2.CHAIN_APPROX_NONE)
            for c in contours:
                area = cv2.contourArea(c)
                if area < 200:
                    continue
                perim = cv2.arcLength(c, True)
                if perim <= 0:
                    continue
                circularity = 4.0 * np.pi * area / (perim * perim)
                if circularity < 0.45:
                    continue
                (_, _), radius = cv2.minEnclosingCircle(c)
                circles.append((area, radius * 2.0, c, th))

        if not circles:
            return {
                "outer_diameter_px": None,
                "inner_diameter_px": None,
                "inner_outer_ratio": None,
                "opening_mask": None,
            }

        circles.sort(key=lambda x: x[0], reverse=True)
        outer_diam_px = float(circles[0][1])
        outer_contour = circles[0][2]
        outer_th = circles[0][3]

        inner_diam_px = None
        outer_area = cv2.contourArea(outer_contour)
        for area, diam_px, c, _ in circles[1:]:
            if area < 0.15 * outer_area:
                continue
            (cx1, cy1), _ = cv2.minEnclosingCircle(outer_contour)
            (cx2, cy2), _ = cv2.minEnclosingCircle(c)
            if np.hypot(cx1 - cx2, cy1 - cy2) <= 0.2 * (outer_diam_px / 2.0):
                inner_diam_px = float(diam_px)
                break

        ratio = None
        if inner_diam_px is not None and outer_diam_px > 0:
            ratio = float(np.clip(inner_diam_px / outer_diam_px, 0.5, 0.99))

        return {
            "outer_diameter_px": outer_diam_px,
            "inner_diameter_px": inner_diam_px,
            "inner_outer_ratio": ratio,
            "opening_mask": outer_th,
        }

    def register_container(
        self,
        frontal_image_path: str,
        top_view_path: Optional[str] = None,
        manual_wall_thickness_mm: Optional[float] = None,
        force_container_type: Optional[str] = None,
        known_height_mm: Optional[float] = None,
        known_rim_diameter_mm: Optional[float] = None,
    ) -> Tuple[str, float, Dict[str, Any]]:
        frontal = cv2.imread(frontal_image_path)
        if frontal is None:
            raise FileNotFoundError(f"Frontal image not found: {frontal_image_path}")

        mask, _ = self.segment_container(frontal)
        profile = self._mask_profile(mask)

        rows = profile["rows"]
        widths_px = profile["widths"]
        radii_px = profile["radii"]
        y_top = profile["y_top"]
        y_bottom = profile["y_bottom"]
        total_height_px = max(1.0, y_bottom - y_top)
        rim_diameter_px = self._robust_band_stat(widths_px, frac=0.06, top=True)
        base_diameter_px = self._robust_band_stat(widths_px, frac=0.06, top=False)

        px_to_mm = self.px_to_mm
        if known_height_mm is not None and total_height_px > 0:
            px_to_mm = float(known_height_mm) / float(total_height_px)
        elif known_rim_diameter_mm is not None and rim_diameter_px > 0:
            px_to_mm = float(known_rim_diameter_mm) / float(rim_diameter_px)
        else:
            warnings.warn(
                "No physical calibration was provided. Absolute volume still depends on fallback px_to_mm.",
                RuntimeWarning,
            )

        rim_diameter_mm = rim_diameter_px * px_to_mm
        base_diameter_mm = base_diameter_px * px_to_mm
        total_height_mm = total_height_px * px_to_mm
        container_type = self._infer_container_type(rim_diameter_mm, base_diameter_mm, total_height_mm, force_container_type)

        top_stats = self._opening_from_top_view(top_view_path)

        if manual_wall_thickness_mm is not None:
            wall_thickness_mm = float(manual_wall_thickness_mm)
        elif top_stats["inner_outer_ratio"] is not None:
            wall_thickness_mm = (rim_diameter_mm * (1.0 - top_stats["inner_outer_ratio"])) / 2.0
        else:
            wall_thickness_mm = 2.5 if container_type == "cup" else 1.8

        wall_thickness_mm = float(np.clip(wall_thickness_mm, 0.8, max(6.0, rim_diameter_mm * 0.12)))
        bottom_thickness_mm = wall_thickness_mm * (1.4 if container_type == "cup" else 1.1)
        usable_height_mm = max(1.0, total_height_mm - bottom_thickness_mm)

        z_mm = (rows - y_top) * px_to_mm
        outer_r_mm = radii_px * px_to_mm
        inner_r_mm = np.maximum(outer_r_mm - wall_thickness_mm, 0.0)

        cavity_z_mm = np.clip(z_mm, 0.0, usable_height_mm)
        valid = (cavity_z_mm >= 0.0) & (cavity_z_mm <= usable_height_mm) & (inner_r_mm >= 0.0)
        cavity_z_mm = cavity_z_mm[valid]
        inner_r_mm = inner_r_mm[valid]

        order = np.argsort(cavity_z_mm)
        cavity_z_mm = cavity_z_mm[order]
        inner_r_mm = inner_r_mm[order]

        uniq_z = np.unique(cavity_z_mm)
        avg_r = [float(np.mean(inner_r_mm[np.isclose(cavity_z_mm, z)])) for z in uniq_z]
        cavity_z_mm = uniq_z
        inner_r_mm = np.asarray(avg_r, dtype=float)

        if cavity_z_mm.size < 5:
            cavity_z_mm = np.linspace(0.0, usable_height_mm, 100)
            inner_base_r = max(base_diameter_mm / 2.0 - wall_thickness_mm, 0.1)
            inner_rim_r = max(rim_diameter_mm / 2.0 - wall_thickness_mm, 0.1)
            inner_r_mm = np.linspace(inner_base_r, inner_rim_r, cavity_z_mm.size)
        else:
            dense_z = np.linspace(0.0, usable_height_mm, 300)
            dense_r = np.interp(dense_z, cavity_z_mm, inner_r_mm)
            cavity_z_mm = dense_z
            inner_r_mm = np.maximum(dense_r, 0.0)

        if top_stats["inner_outer_ratio"] is not None:
            target_rim_inner_mm = (rim_diameter_mm * top_stats["inner_outer_ratio"]) / 2.0
            current_rim = max(inner_r_mm[-1], 1e-6)
            scale = target_rim_inner_mm / current_rim
            if 0.7 <= scale <= 1.2:
                inner_r_mm *= scale
            else:
                inner_r_mm[-20:] = np.linspace(inner_r_mm[-20], target_rim_inner_mm, 20)

        capacity_ml = float(integrate.trapezoid(np.pi * (inner_r_mm ** 2), cavity_z_mm) / 1000.0)

        def r_inner(z_val: float) -> float:
            z_val = float(np.clip(z_val, 0.0, usable_height_mm))
            return float(np.interp(z_val, cavity_z_mm, inner_r_mm))

        container_data = {
            "container_type": container_type,
            "rim_diameter_mm": rim_diameter_mm,
            "base_diameter_mm": base_diameter_mm,
            "total_height_mm": total_height_mm,
            "effective_height_mm": usable_height_mm,
            "wall_thickness_mm": wall_thickness_mm,
            "bottom_thickness_mm": bottom_thickness_mm,
            "inner_rim_diameter_mm": 2.0 * inner_r_mm[-1],
            "capacity_ml": capacity_ml,
            "r_inner_function": r_inner,
            "z_samples_mm": cavity_z_mm,
            "r_samples_mm": inner_r_mm,
            "top_view_inner_outer_ratio": top_stats["inner_outer_ratio"],
            "px_to_mm_used": px_to_mm,
        }
        return container_type, capacity_ml, container_data

    def estimate_liquid_volume(self, container_data: Dict[str, Any], liquid_top_view_path: str) -> Dict[str, float]:
        img = cv2.imread(liquid_top_view_path)
        if img is None:
            raise FileNotFoundError(f"Liquid top-view image not found: {liquid_top_view_path}")

        gray = self.preprocess(img)
        candidates = []
        for mode in [cv2.THRESH_BINARY + cv2.THRESH_OTSU, cv2.THRESH_BINARY_INV + cv2.THRESH_OTSU]:
            _, th = cv2.threshold(gray, 0, 255, mode)
            th = cv2.morphologyEx(th, cv2.MORPH_OPEN, np.ones((3, 3), np.uint8), iterations=1)
            th = cv2.morphologyEx(th, cv2.MORPH_CLOSE, np.ones((5, 5), np.uint8), iterations=2)
            contours, _ = cv2.findContours(th, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_NONE)
            for c in contours:
                area = cv2.contourArea(c)
                if area < 100:
                    continue
                perim = cv2.arcLength(c, True)
                if perim <= 0:
                    continue
                circularity = 4.0 * np.pi * area / (perim * perim)
                if circularity < 0.4:
                    continue
                (_, _), radius = cv2.minEnclosingCircle(c)
                candidates.append((area, radius))

        if not candidates:
            raise ValueError("No liquid surface detected in top view")

        candidates.sort(key=lambda x: x[0], reverse=True)
        liquid_radius_px = float(candidates[0][1])
        px_to_mm = float(container_data.get("px_to_mm_used", self.px_to_mm))
        liquid_radius_mm = liquid_radius_px * px_to_mm

        z_samples = np.asarray(container_data["z_samples_mm"], dtype=float)
        r_samples = np.asarray(container_data["r_samples_mm"], dtype=float)
        capacity_ml = float(container_data["capacity_ml"])

        idx = int(np.argmin(np.abs(r_samples - liquid_radius_mm)))
        fill_height_mm = float(z_samples[idx])
        fill_volume_ml = float(integrate.trapezoid(np.pi * (r_samples[: idx + 1] ** 2), z_samples[: idx + 1]) / 1000.0)
        fill_pct = 100.0 * fill_volume_ml / max(capacity_ml, 1e-6)

        return {
            "container_type": container_data["container_type"],
            "curry_volume_ml": fill_volume_ml,
            "fill_height_mm": fill_height_mm,
            "fill_percentage": fill_pct,
            "surface_radius_mm": liquid_radius_mm,
            "container_capacity_ml": capacity_ml,
        }


