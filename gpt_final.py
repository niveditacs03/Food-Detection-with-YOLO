class IntegratedFoodEstimator:
    def __init__(self, yolo_model_path: str, px_to_mm: float = 0.3438):
        self.yolo_model = YOLO(yolo_model_path)
        self.classifier = FoodCategoryClassifier()
        self.liquid_estimator = LiquidVolumeEstimator(px_to_mm=px_to_mm)
        self.portion_estimator = PortionEstimator()
        self.last_prediction: Optional[Dict[str, Any]] = None

    def predict(self, image_path: str, conf_threshold: float = 0.25) -> Dict[str, Any]:
        if not os.path.exists(image_path):
            raise FileNotFoundError(f"Image not found: {image_path}")
        results = self.yolo_model.predict(source=image_path, conf=conf_threshold, iou=0.45, save=False)
        predictions: List[Dict[str, Any]] = []
        for r in results:
            if hasattr(r, 'boxes') and r.boxes is not None:
                for box in r.boxes:
                    class_id = int(box.cls[0])
                    confidence = float(box.conf[0])
                    class_name = self.yolo_model.names[class_id]
                    predictions.append({
                        'class_id': class_id,
                        'class_name': class_name,
                        'confidence': confidence,
                    })
        if not predictions:
            raise ValueError("No food items detected in the image")

        class_counts = Counter(pred['class_name'] for pred in predictions)
        top_prediction = max(predictions, key=lambda x: x['confidence'])
        top_category, top_method = self.classifier.classify_food(top_prediction['class_name'])
        self.last_prediction = {
            'class_name': top_prediction['class_name'],
            'class_id': top_prediction['class_id'],
            'confidence': top_prediction['confidence'],
            'category': top_category,
            'estimation_method': top_method,
            'all_detections': predictions,
            'class_counts': dict(class_counts),
            'total_items': len(predictions),
        }
        return self.last_prediction

    def estimate(
        self,
        image_path: str,
        container_frontal_path: Optional[str] = None,
        container_top_view_path: Optional[str] = None,
        liquid_surface_top_view_path: Optional[str] = None,
        auto_mask: bool = True,
        predicted_class: Optional[str] = None,
        known_height_mm: Optional[float] = None,
        known_rim_diameter_mm: Optional[float] = None,
    ) -> Dict[str, Any]:
        if predicted_class is None:
            if self.last_prediction is None:
                raise ValueError("No prediction available. Call predict() first or provide predicted_class.")
            predicted_class = self.last_prediction['class_name']
            class_counts = self.last_prediction.get('class_counts', {})
        else:
            class_counts = self.last_prediction.get('class_counts', {predicted_class: 1}) if self.last_prediction else {predicted_class: 1}

        category, method = self.classifier.classify_food(predicted_class)

        if method == 'portion_estimation':
            return self._estimate_rice(predicted_class, image_path, auto_mask)
        if method == 'liquid_estimation_bowl':
            if container_frontal_path is None:
                raise ValueError("Container frontal image required for liquid estimation")
            return self._estimate_liquid(
                predicted_class, 'bowl', container_frontal_path, container_top_view_path,
                liquid_surface_top_view_path, known_height_mm, known_rim_diameter_mm,
            )
        if method == 'liquid_estimation_cup':
            if container_frontal_path is None:
                raise ValueError("Container frontal image required for liquid estimation")
            return self._estimate_liquid(
                predicted_class, 'cup', container_frontal_path, container_top_view_path,
                liquid_surface_top_view_path, known_height_mm, known_rim_diameter_mm,
            )
        if method == 'item_counting':
            return self._estimate_items(predicted_class, class_counts)
        return {'status': 'not_implemented', 'predicted_class': predicted_class, 'category': category}

    def _estimate_rice(self, food_name: str, image_path: str, auto_mask: bool) -> Dict[str, Any]:
        density = FOOD_DENSITY_MAP.get(food_name.lower().strip(), FOOD_DENSITY_MAP['default'])
        if not self.portion_estimator.model_loaded:
            self.portion_estimator.load_midas_model(model_type="DPT_Hybrid")
        depth_map, img_rgb = self.portion_estimator.generate_depth_map(image_path)
        mask = self.portion_estimator.create_food_mask(depth_map=depth_map, auto_mask=auto_mask)
        metrics = self.portion_estimator.calculate_portion_metrics(
            depth_map=depth_map,
            mask=mask,
            plate_real_diameter_cm=25,
            plate_pixel_diameter=520,
            max_food_height_cm=2.0,
            density=density,
        )
        return {
            'status': 'success',
            'predicted_class': food_name,
            'method': 'portion_estimation',
            'results': metrics,
        }

    def _estimate_liquid(
        self,
        food_name: str,
        container_type_hint: str,
        frontal_path: str,
        top_view_path: Optional[str],
        liquid_surface_top_view_path: Optional[str],
        known_height_mm: Optional[float],
        known_rim_diameter_mm: Optional[float],
    ) -> Dict[str, Any]:
        container_type, capacity_ml, container_data = self.liquid_estimator.register_container(
            frontal_image_path=frontal_path,
            top_view_path=top_view_path,
            force_container_type=container_type_hint,
            known_height_mm=known_height_mm,
            known_rim_diameter_mm=known_rim_diameter_mm,
        )

        if liquid_surface_top_view_path is not None:
            liquid_stats = self.liquid_estimator.estimate_liquid_volume(container_data, liquid_surface_top_view_path)
            estimated_volume_ml = liquid_stats['curry_volume_ml']
            fill_height_mm = liquid_stats['fill_height_mm']
            fill_percentage = liquid_stats['fill_percentage']
            surface_radius_mm = liquid_stats['surface_radius_mm']
            note = 'Fill-volume estimated from separate liquid-surface top view.'
        else:
            estimated_volume_ml = capacity_ml
            fill_height_mm = None
            fill_percentage = None
            surface_radius_mm = None
            note = 'Capacity only. Provide liquid_surface_top_view_path to estimate actual fill-volume.'

        return {
            'status': 'success',
            'predicted_class': food_name,
            'method': f'liquid_estimation_{container_type_hint}',
            'results': {
                'container_type': container_type,
                'estimated_volume_ml': float(estimated_volume_ml),
                'curry_volume_ml': float(estimated_volume_ml),
                'container_capacity_ml': float(capacity_ml),
                'fill_height_mm': fill_height_mm,
                'fill_percentage': fill_percentage,
                'surface_radius_mm': surface_radius_mm,
                'geometry_debug': {
                    'rim_diameter_mm': container_data['rim_diameter_mm'],
                    'base_diameter_mm': container_data['base_diameter_mm'],
                    'effective_height_mm': container_data['effective_height_mm'],
                    'wall_thickness_mm': container_data['wall_thickness_mm'],
                    'px_to_mm_used': container_data['px_to_mm_used'],
                },
                'note': note,
            },
        }

    def _estimate_items(self, food_name: str, class_counts: Dict[str, int]) -> Dict[str, Any]:
        items_in_category: Dict[str, int] = {}
        total_count = 0
        estimated_weight_g = 0.0
        for item_name, count in class_counts.items():
            category, _ = self.classifier.classify_food(item_name)
            if category == 'items':
                items_in_category[item_name] = count
                total_count += count
                estimated_weight_g += ITEM_AVG_WEIGHT_G.get(item_name.lower().strip(), 0.0) * count
        return {
            'status': 'success',
            'predicted_class': food_name,
            'method': 'item_counting',
            'results': {
                'total_items': total_count,
                'item_counts': items_in_category,
                'all_detections': class_counts,
                'estimated_weight_g': estimated_weight_g,
            },
        }

    def predict_and_estimate(
        self,
        image_path: str,
        container_frontal_path: Optional[str] = None,
        container_top_view_path: Optional[str] = None,
        liquid_surface_top_view_path: Optional[str] = None,
        auto_mask: bool = True,
        known_height_mm: Optional[float] = None,
        known_rim_diameter_mm: Optional[float] = None,
    ) -> Dict[str, Any]:
        prediction = self.predict(image_path)
        estimation = self.estimate(
            image_path=image_path,
            container_frontal_path=container_frontal_path,
            container_top_view_path=container_top_view_path,
            liquid_surface_top_view_path=liquid_surface_top_view_path,
            auto_mask=auto_mask,
            known_height_mm=known_height_mm,
            known_rim_diameter_mm=known_rim_diameter_mm,
        )
        return {'prediction': prediction, 'estimation': estimation}


