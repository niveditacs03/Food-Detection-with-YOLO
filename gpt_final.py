class DataProcessor:
    def __init__(self, file_path: str, column_mapping: Dict[str, str]):
        self.file_path = file_path
        self.column_mapping = column_mapping
        self.df: Optional[pd.DataFrame] = None
        self.processed_df: Optional[pd.DataFrame] = None

    def load_data(self) -> pd.DataFrame:
        if not os.path.exists(self.file_path):
            raise FileNotFoundError(f"{self.file_path} not found")
        self.df = pd.read_excel(self.file_path)
        return self.df

    def preprocess(self) -> pd.DataFrame:
        if self.df is None:
            raise ValueError("Data not loaded. Call load_data() first.")

        mapped_cols: Dict[str, str] = {}
        for logical_name, excel_header in self.column_mapping.items():
            logical_name = logical_name.strip()
            excel_header = excel_header.strip()
            if excel_header not in self.df.columns:
                found = False
                for col in self.df.columns:
                    if excel_header.split()[0].lower() in col.lower():
                        mapped_cols[logical_name] = col
                        found = True
                        break
                if not found:
                    raise ValueError(f"Column '{excel_header}' not found in dataset. Update column_mapping.")
            else:
                mapped_cols[logical_name] = excel_header

        self.processed_df = self.df[list(mapped_cols.values())].copy()
        self.processed_df.columns = list(mapped_cols.keys())
        self.processed_df['food_name_clean'] = self.processed_df['food_name'].apply(self._normalize_text)

        nutrient_cols = ['calories', 'protein', 'carbs', 'fats']
        self.processed_df = self.processed_df.dropna(subset=nutrient_cols)
        for col in nutrient_cols:
            self.processed_df[col] = pd.to_numeric(self.processed_df[col], errors='coerce')
        self.processed_df = self.processed_df.dropna(subset=nutrient_cols)
        return self.processed_df

    @staticmethod
    def _normalize_text(text: str) -> str:
        if not isinstance(text, str):
            return ""
        text = text.lower()
        text = re.sub(r'[^\w\s]', '', text)
        text = re.sub(r'\s+', ' ', text).strip()
        return text


class EmbeddingEngine:
    def __init__(self, model_name: str, device: str):
        self.model = SentenceTransformer(model_name, device=device)
        self.dimension = self.model.get_sentence_embedding_dimension()

    def encode(self, texts: List[str]) -> np.ndarray:
        embeddings = self.model.encode(texts, convert_to_numpy=True, normalize_embeddings=True)
        return embeddings.astype('float32')


class VectorStore:
    def __init__(self, dimension: int):
        self.index = faiss.IndexFlatIP(dimension)
        self.metadata: List[Dict[str, Any]] = []

    def add(self, embeddings: np.ndarray, metadata: List[Dict[str, Any]]) -> None:
        if len(embeddings) != len(metadata):
            raise ValueError("Embeddings and metadata length mismatch")
        self.index.add(embeddings)
        self.metadata.extend(metadata)

    def search(self, query_embedding: np.ndarray, top_k: int) -> Tuple[List[Dict[str, Any]], List[float]]:
        distances, indices = self.index.search(query_embedding, top_k)
        results, scores = [], []
        for i, idx in enumerate(indices[0]):
            if idx == -1:
                continue
            results.append(self.metadata[idx])
            scores.append(float(distances[0][i]))
        return results, scores


class XGBoostPredictor:
    def __init__(self, nutrient_cols: List[str], config: NutritionConfig):
        self.nutrient_cols = nutrient_cols
        self.config = config
        self.models: Dict[str, xgb.XGBRegressor] = {}
        self.is_trained = False

    def train(self, embeddings: np.ndarray, df: pd.DataFrame) -> None:
        X = embeddings
        y = df[self.nutrient_cols].values
        X_train, X_test, y_train, y_test = train_test_split(
            X, y, test_size=0.2, random_state=self.config.random_state
        )
        for i, col in enumerate(self.nutrient_cols):
            model = xgb.XGBRegressor(
                objective='reg:squarederror',
                n_estimators=300,
                max_depth=6,
                learning_rate=0.08,
                random_state=self.config.random_state,
                device=self.config.device,
            )
            model.fit(X_train, y_train[:, i])
            self.models[col] = model
            _ = np.sqrt(mean_squared_error(y_test[:, i], model.predict(X_test)))
        self.is_trained = True

    def predict(self, embedding: np.ndarray) -> Dict[str, float]:
        if not self.is_trained:
            raise RuntimeError("XGBoost models not trained yet")
        if embedding.ndim == 1:
            embedding = embedding.reshape(1, -1)
        return {col: max(0.0, float(model.predict(embedding)[0])) for col, model in self.models.items()}


class LLMEngine:
    def __init__(self, model_name: str, device: str):
        self.tokenizer = AutoTokenizer.from_pretrained(model_name, trust_remote_code=True)
        self.model = AutoModelForCausalLM.from_pretrained(
            model_name,
            device_map="auto" if device == "cuda" else None,
            trust_remote_code=True,
            torch_dtype=torch.float16 if device == "cuda" else torch.float32,
        )
        self.pipe = pipeline(
            "text-generation",
            model=self.model,
            tokenizer=self.tokenizer,
            max_new_tokens=256,
            do_sample=False,
            temperature=0.0,
        )

    def generate(self, prompt: str) -> str:
        messages = [
            {"role": "system", "content": "You are a precise Nutrition Assistant. Output only the requested structured fields."},
            {"role": "user", "content": prompt},
        ]
        response = self.pipe(messages)
        return response[0]['generated_text'][-1]['content']


class NutritionRAGSystem:
    def __init__(self, config: NutritionConfig, load_llm: bool = False):
        self.config = config
        self.data_processor = DataProcessor(self.config.indb_file_path, self.config.column_mapping)
        self.embedding_engine: Optional[EmbeddingEngine] = None
        self.vector_store: Optional[VectorStore] = None
        self.xgb_predictor: Optional[XGBoostPredictor] = None
        self.llm_engine: Optional[LLMEngine] = None
        self.is_ready = False
        self.load_llm = load_llm

    def initialize(self) -> None:
        self.data_processor.load_data()
        df = self.data_processor.preprocess()
        self.embedding_engine = EmbeddingEngine(self.config.embedding_model_name, self.config.device)
        self.vector_store = VectorStore(self.embedding_engine.dimension)
        food_names = df['food_name_clean'].tolist()
        embeddings = self.embedding_engine.encode(food_names)
        self.vector_store.add(embeddings, df.to_dict('records'))
        self.xgb_predictor = XGBoostPredictor(['calories', 'protein', 'carbs', 'fats'], self.config)
        self.xgb_predictor.train(embeddings, df)
        if self.load_llm:
            self.llm_engine = LLMEngine(self.config.llm_model_name, self.config.device)
        self.is_ready = True

    def get_nutrient_data(self, food_name: str) -> Dict[str, Any]:
        if not self.is_ready:
            raise RuntimeError("NutritionRAGSystem is not initialized")
        assert self.embedding_engine is not None and self.vector_store is not None and self.xgb_predictor is not None
        query_embedding = self.embedding_engine.encode([DataProcessor._normalize_text(food_name)])
        retrieved_docs, similarity_scores = self.vector_store.search(query_embedding, self.config.top_k_retrieval)
        if retrieved_docs and similarity_scores[0] >= self.config.similarity_threshold:
            best = dict(retrieved_docs[0])
            best['source'] = 'Indian Nutrient Databank'
            best['match_similarity'] = float(similarity_scores[0])
            best['route'] = 'rag'
            return best
        predicted_vals = self.xgb_predictor.predict(query_embedding)
        predicted_vals['source'] = 'Estimated using ML model (XGBoost)'
        predicted_vals['match_similarity'] = float(similarity_scores[0]) if similarity_scores else 0.0
        predicted_vals['route'] = 'xgboost_fallback'
        return predicted_vals

    def query(self, food_name: str) -> Dict[str, Any]:
        return self.get_nutrient_data(food_name)


