class PortionEstimator:
    def __init__(self, device: Optional[torch.device] = None):
        self.device = torch.device("cuda" if torch.cuda.is_available() else "cpu") if device is None else device
        self.midas = None
        self.midas_transform = None
        self.model_loaded = False
        self.last_depth_map = None
        self.last_mask = None
        self.last_image = None

    def load_midas_model(self, model_type: str = "DPT_Hybrid") -> bool:
        self.midas = torch.hub.load("intel-isl/MiDaS", model_type)
        self.midas.to(self.device)
        self.midas.eval()
        midas_transforms = torch.hub.load("intel-isl/MiDaS", "transforms")
        self.midas_transform = midas_transforms.dpt_transform
        self.model_loaded = True
        return self.model_loaded

    def generate_depth_map(self, image_path: str) -> Tuple[np.ndarray, np.ndarray]:
        if not self.model_loaded:
            raise RuntimeError("MiDaS model not loaded")
        img = cv2.imread(image_path)
        if img is None:
            raise FileNotFoundError(f"Image not found: {image_path}")
        img_rgb = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)
        self.last_image = img_rgb
        input_batch = self.midas_transform(img_rgb).to(self.device)
        with torch.no_grad():
            prediction = self.midas(input_batch)
        depth_map = prediction.squeeze().cpu().numpy()
        depth_map = cv2.resize(depth_map, (img.shape[1], img.shape[0]))
        self.last_depth_map = depth_map
        return depth_map, img_rgb

    def create_food_mask(
        self,
        depth_map: Optional[np.ndarray] = None,
        auto_mask: bool = True,
        x1: Optional[int] = None,
        y1: Optional[int] = None,
        x2: Optional[int] = None,
        y2: Optional[int] = None,
        h_ratio: float = 0.20,
        w_ratio: float = 0.80,
    ) -> np.ndarray:
        if depth_map is None:
            depth_map = self.last_depth_map
        h, w = depth_map.shape
        if auto_mask:
            mask = np.zeros((h, w), dtype=np.uint8)
            mask[int(h * h_ratio):int(h * w_ratio), int(w * h_ratio):int(w * w_ratio)] = 1
        else:
            if None in (x1, y1, x2, y2):
                raise ValueError("Manual coordinates required when auto_mask=False")
            mask = np.zeros(depth_map.shape, dtype=np.uint8)
            mask[y1:y2, x1:x2] = 1
        self.last_mask = mask
        return mask

    def calculate_portion_metrics(
        self,
        depth_map: Optional[np.ndarray] = None,
        mask: Optional[np.ndarray] = None,
        plate_real_diameter_cm: float = 25,
        plate_pixel_diameter: float = 520,
        max_food_height_cm: float = 2.0,
        density: float = 0.45,
    ) -> Dict[str, float]:
        if depth_map is None:
            depth_map = self.last_depth_map
        if mask is None:
            mask = self.last_mask
        scale_cm_per_pixel = plate_real_diameter_cm / plate_pixel_diameter
        depth_norm = (depth_map - depth_map.min()) / (depth_map.max() - depth_map.min() + 1e-8)
        food_depth = np.mean(depth_norm[mask == 1])
        plate_depth = np.mean(depth_norm)
        relative_thickness = abs(plate_depth - food_depth)
        thickness_cm = relative_thickness * max_food_height_cm
        pixel_area = np.sum(mask)
        area_cm2 = pixel_area * (scale_cm_per_pixel ** 2)
        volume_cm3 = area_cm2 * thickness_cm
        weight_g = volume_cm3 * density
        return {
            'scale_cm_per_pixel': float(scale_cm_per_pixel),
            'relative_thickness': float(relative_thickness),
            'thickness_cm': float(thickness_cm),
            'area_cm2': float(area_cm2),
            'volume_cm3': float(volume_cm3),
            'weight_g': float(weight_g),
        }


