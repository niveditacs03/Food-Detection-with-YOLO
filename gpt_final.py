class IntegratedNutritionVolumeSystem:
    """Merged pipeline requested by the user.

    Flow:
    YOLO prediction -> improved volume/weight estimation -> RAG/XGBoost nutrient lookup
    -> serving-level calories/protein/carbs/fats from estimated amount.
    """

    def __init__(
        self,
        yolo_model_path: str,
        indb_file_path: str,
        column_mapping: Dict[str, str],
        px_to_mm: float = 0.3438,
        similarity_threshold: float = 0.75,
        load_llm: bool = False,
    ):
        self.volume_estimator = IntegratedFoodEstimator(yolo_model_path=yolo_model_path, px_to_mm=px_to_mm)
        self.nutrition_system = NutritionRAGSystem(
            NutritionConfig(
                indb_file_path=indb_file_path,
                similarity_threshold=similarity_threshold,
                column_mapping=column_mapping,
            ),
            load_llm=load_llm,
        )
        self.nutrition_system.initialize()

    @staticmethod
    def _food_density(food_name: str) -> float:
        return FOOD_DENSITY_MAP.get(food_name.lower().strip(), FOOD_DENSITY_MAP['default'])

    def _estimate_serving_mass_g(self, food_name: str, method: str, est: Dict[str, Any]) -> Tuple[float, Dict[str, Any]]:
        results = est['results']
        meta: Dict[str, Any] = {}

        if method == 'portion_estimation':
            mass_g = float(results.get('weight_g', 0.0))
            meta['mass_source'] = 'direct_weight_from_depth_volume'
            return mass_g, meta

        if method in ['liquid_estimation_bowl', 'liquid_estimation_cup']:
            volume_ml = float(results.get('curry_volume_ml', results.get('estimated_volume_ml', 0.0)))
            density = self._food_density(food_name)
            mass_g = volume_ml * density
            meta['volume_ml'] = volume_ml
            meta['density_g_per_ml'] = density
            meta['mass_source'] = 'volume_times_density'
            return mass_g, meta

        if method == 'item_counting':
            mass_g = float(results.get('estimated_weight_g', 0.0))
            meta['mass_source'] = 'item_count_times_avg_item_weight'
            if mass_g == 0.0:
                meta['warning'] = 'No fallback average item weight available for this class.'
            return mass_g, meta

        meta['mass_source'] = 'unavailable'
        return 0.0, meta

    @staticmethod
    def _scale_per_100g(nutrient_data: Dict[str, Any], serving_mass_g: float) -> Dict[str, float]:
        factor = serving_mass_g / 100.0
        return {
            'serving_mass_g': float(serving_mass_g),
            'calories_kcal': float(nutrient_data.get('calories', 0.0)) * factor,
            'protein_g': float(nutrient_data.get('protein', 0.0)) * factor,
            'carbs_g': float(nutrient_data.get('carbs', 0.0)) * factor,
            'fats_g': float(nutrient_data.get('fats', 0.0)) * factor,
        }

    def process_image(
        self,
        image_path: str,
        container_frontal_path: Optional[str] = None,
        container_top_view_path: Optional[str] = None,
        liquid_surface_top_view_path: Optional[str] = None,
        auto_mask: bool = True,
        known_height_mm: Optional[float] = None,
        known_rim_diameter_mm: Optional[float] = None,
        conf_threshold: float = 0.25,
    ) -> Dict[str, Any]:
        prediction = self.volume_estimator.predict(image_path=image_path, conf_threshold=conf_threshold)
        estimation = self.volume_estimator.estimate(
            image_path=image_path,
            container_frontal_path=container_frontal_path,
            container_top_view_path=container_top_view_path,
            liquid_surface_top_view_path=liquid_surface_top_view_path,
            auto_mask=auto_mask,
            known_height_mm=known_height_mm,
            known_rim_diameter_mm=known_rim_diameter_mm,
        )

        food_name = prediction['class_name']
        method = prediction['estimation_method']
        nutrient_data = self.nutrition_system.get_nutrient_data(food_name)
        serving_mass_g, mass_meta = self._estimate_serving_mass_g(food_name, method, estimation)
        serving_macros = self._scale_per_100g(nutrient_data, serving_mass_g)

        result = {
            'food_name': food_name,
            'prediction': prediction,
            'estimation': estimation,
            'nutrition_lookup': {
                'route': nutrient_data.get('route'),
                'source': nutrient_data.get('source'),
                'match_similarity': nutrient_data.get('match_similarity'),
                'per_100g': {
                    'calories_kcal': float(nutrient_data.get('calories', 0.0)),
                    'protein_g': float(nutrient_data.get('protein', 0.0)),
                    'carbs_g': float(nutrient_data.get('carbs', 0.0)),
                    'fats_g': float(nutrient_data.get('fats', 0.0)),
                },
            },
            'serving_estimate': {
                **serving_macros,
                **mass_meta,
            },
        }

        return result

